public class Var_test {
    public static void main(String[] args) {
        int a = 10; // 정수형 변수 a 선언 및 초기화
        double b = 3.14; // 실수형 변수 b 선언 및 초기화
        String c = "Hello, World!"; // 문자열 변수 c 선언 및 초기화

        System.out.println("a: " + a); // a의 값 출력
        System.out.println("b: " + b); // b의 값 출력
        System.out.println("c: " + c); // c의 값 출력
    }
    
}
