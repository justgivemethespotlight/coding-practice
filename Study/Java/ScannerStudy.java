public class ScannerStudy {
    public static void main(String[] args) {
        System.out.println((int)'A');   // 65
        System.out.println((int)'a');   // 97
        System.out.println((int)'0');   // 48
        System.out.println((char)66);   // B
        System.out.println((char)97);   // a
    }
}
